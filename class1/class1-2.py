# Calculate the area of a rhombus using user input

# Get user input for diagonals
d1 = float(input("Please enter the length of the first diagonal: "))
d2 = float(input("Please enter the length of the second diagonal: "))

# Calculate area of the rhombus
a = (d1 * d2) / 2

# Display the result
print(f"The area of the rhombus is {a}")
